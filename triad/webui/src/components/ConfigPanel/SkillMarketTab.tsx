// Main Component: SkillMarketTab
// ─────────────────────────────────────────────

const SkillMarketTab: React.FC = () => {
  // -- State --
  const [activeTab, setActiveTab] = useState<'tools' | 'skills'>('tools');
  const [searchQuery, setSearchQuery] = useState('');
  const [activeCategory, setActiveCategory] = useState('all');

  const [toolStates, setToolStates] = useState<Record<string, boolean>>(
    Object.fromEntries(mcpTools.map((t) => [t.id, t.enabled]))
  );
  const [skillStates, setSkillStates] = useState<Record<string, boolean>>(
    Object.fromEntries(skills.map((s) => [s.id, s.enabled]))
  );

  const [toast, setToast] = useState<ToastState>({
    message: '',
    type: 'success',
    visible: false,
  });

  // -- Categories per tab --
  const toolCategories = useMemo(() => {
    const cats = new Set(mcpTools.map((t) => t.category));
    return ['all', ...Array.from(cats)];
  }, []);

  const skillCategories = useMemo(() => {
    const cats = new Set(skills.map((s) => s.category));
    return ['all', ...Array.from(cats)];
  }, []);

  const currentCategories = activeTab === 'tools' ? toolCategories : skillCategories;

  // -- Filtered items --
  const filteredTools = useMemo(() => {
    return mcpTools.filter((tool) => {
      const matchesSearch =
        !searchQuery ||
        tool.name.toLowerCase().includes(searchQuery.toLowerCase()) ||
        tool.description.toLowerCase().includes(searchQuery.toLowerCase());
      const matchesCategory =
        activeCategory === 'all' || tool.category === activeCategory;
      return matchesSearch && matchesCategory;
    });
  }, [searchQuery, activeCategory]);

  const filteredSkills = useMemo(() => {
    return skills.filter((skill) => {
      const matchesSearch =
        !searchQuery ||
        skill.name.toLowerCase().includes(searchQuery.toLowerCase()) ||
        skill.description.toLowerCase().includes(searchQuery.toLowerCase());
      const matchesCategory =
        activeCategory === 'all' || skill.category === activeCategory;
      return matchesSearch && matchesCategory;
    });
  }, [searchQuery, activeCategory]);

  // -- Toggle handler --
  const showToast = useCallback((message: string, type: ToastType) => {
    setToast({ message, type, visible: true });
  }, []);

  const dismissToast = useCallback(() => {
    setToast((prev) => ({ ...prev, visible: false }));
  }, []);

  const handleToggleTool = useCallback(
    (id: string, enabled: boolean) => {
      setToolStates((prev) => ({ ...prev, [id]: enabled }));
      const tool = mcpTools.find((t) => t.id === id);
      const name = tool?.name || id;
      // Simulate API call
      setTimeout(() => {
        showToast(
          `${enabled ? '已启用' : '已禁用'} MCP 插件：${name}`,
          'success'
        );
      }, 200);
    },
    [showToast]
  );

  const handleToggleSkill = useCallback(
    (id: string, enabled: boolean) => {
      setSkillStates((prev) => ({ ...prev, [id]: enabled }));
      const skill = skills.find((s) => s.id === id);
      const name = skill?.name || id;
      // Simulate API call
      setTimeout(() => {
        showToast(
          `${enabled ? '已启用' : '已禁用'} 技能：${name}`,
          'success'
        );
      }, 200);
    },
    [showToast]
  );

  // -- Reset category on tab switch --
  const handleTabChange = useCallback((value: string) => {
    setActiveTab(value as 'tools' | 'skills');
    setActiveCategory('all');
    setSearchQuery('');
  }, []);

  return (
    <div className="w-full h-full flex flex-col">
      {/* ── Header ── */}
      <div className="shrink-0 mb-5">
        <h2 className="text-lg font-bold text-foreground flex items-center gap-2 mb-4">
          <Plug className="w-5 h-5 text-primary" />
          技能与插件市场
        </h2>

        {/* Tabs */}
        <Tabs
          value={activeTab}
          onValueChange={handleTabChange}
        >
          <TabsList className="w-full grid grid-cols-2 h-11">
            <TabsTrigger
              value="tools"
              className="flex items-center gap-2 text-sm font-medium data-[state=active]:bg-background data-[state=active]:shadow-sm"
            >
              <Plug className="w-4 h-4" />
              MCP 硬件外设
              <span className="text-[10px] text-muted-foreground ml-1">
                ({mcpTools.length})
              </span>
            </TabsTrigger>
            <TabsTrigger
              value="skills"
              className="flex items-center gap-2 text-sm font-medium data-[state=active]:bg-background data-[state=active]:shadow-sm"
            >
              <Brain className="w-4 h-4" />
              大脑认知技能
              <span className="text-[10px] text-muted-foreground ml-1">
                ({skills.length})
              </span>
            </TabsTrigger>
          </TabsList>
        </Tabs>
      </div>

      {/* ── Search & Filter Bar ── */}
      <div className="shrink-0 flex flex-col gap-3 mb-4">
        <div className="relative">
          <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-muted-foreground pointer-events-none" />
          <Input
            type="text"
            placeholder={
              activeTab === 'tools'
                ? '搜索 MCP 插件名称或描述...'
                : '搜索技能名称或描述...'
            }
            value={searchQuery}
            onChange={(e) => setSearchQuery(e.target.value)}
            className="pl-9 h-10 text-sm bg-muted/40 border-muted focus:bg-background"
          />
        </div>

        <CategoryTabBar
          categories={currentCategories}
          active={activeCategory}
          onSelect={setActiveCategory}
        />
      </div>

      {/* ── Content ── */}
      <div className="flex-1 overflow-y-auto min-h-0 pr-1">
        {/* Tools Tab Content */}
        {activeTab === 'tools' && (
          <div>
            <StatsSummary
              total={filteredTools.length}
              enabled={filteredTools.filter((t) => toolStates[t.id]).length}
              label="插件"
            />
            {filteredTools.length === 0 ? (
              <div className="flex flex-col items-center justify-center py-16 text-muted-foreground">
                <Plug className="w-10 h-10 mb-3 opacity-40" />
                <p className="text-sm">未找到匹配的 MCP 插件</p>
                <p className="text-xs mt-1 opacity-60">
                  尝试调整搜索关键词或分类筛选
                </p>
              </div>
            ) : (
              <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-4 pb-4">
                {filteredTools.map((tool) => (
                  <CardItem
                    key={tool.id}
                    id={tool.id}
                    name={tool.name}
                    description={tool.description}
                    category={tool.category}
                    icon={tool.icon}
                    enabled={toolStates[tool.id] ?? tool.enabled}
                    source={tool.source}
                    onToggle={handleToggleTool}
                  />
                ))}
              </div>
            )}
          </div>
        )}

        {/* Skills Tab Content */}
        {activeTab === 'skills' && (
          <div>
            <StatsSummary
              total={filteredSkills.length}
              enabled={filteredSkills.filter((s) => skillStates[s.id]).length}
              label="技能"
            />
            {filteredSkills.length === 0 ? (
              <div className="flex flex-col items-center justify-center py-16 text-muted-foreground">
                <Brain className="w-10 h-10 mb-3 opacity-40" />
                <p className="text-sm">未找到匹配的技能</p>
                <p className="text-xs mt-1 opacity-60">
                  尝试调整搜索关键词或分类筛选
                </p>
              </div>
            ) : (
              <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-4 pb-4">
                {filteredSkills.map((skill) => (
                  <CardItem
                    key={skill.id}
                    id={skill.id}
                    name={skill.name}
                    description={skill.description}
                    category={skill.category}
                    icon={skill.icon}
                    enabled={skillStates[skill.id] ?? skill.enabled}
                    autoGenerated={skill.autoGenerated}
                    source={skill.source}
                    onToggle={handleToggleSkill}
                  />
                ))}
              </div>
            )}
          </div>
        )}
      </div>

      {/* ── Toast ── */}
      <Toast toast={toast} onDismiss={dismissToast} />
    </div>
  );
};

export default SkillMarketTab;
