// ─────────────────────────────────────────────
// Toast Component
// ─────────────────────────────────────────────

interface ToastProps {
  toast: ToastState;
  onDismiss: () => void;
}

const Toast: React.FC<ToastProps> = ({ toast, onDismiss }) => {
  useEffect(() => {
    if (toast.visible) {
      const timer = setTimeout(onDismiss, 2500);
      return () => clearTimeout(timer);
    }
  }, [toast.visible, onDismiss]);

  if (!toast.visible) return null;

  return (
    <div className="fixed bottom-6 right-6 z-50 animate-in slide-in-from-bottom-2 fade-in duration-300">
      <div
        className={`
          flex items-center gap-2 px-4 py-3 rounded-lg shadow-lg border text-sm font-medium
          ${toast.type === 'success'
            ? 'bg-emerald-50 text-emerald-700 border-emerald-200'
            : 'bg-red-50 text-red-700 border-red-200'
          }
        `}
      >
        <span
          className={`w-2 h-2 rounded-full ${toast.type === 'success' ? 'bg-emerald-500' : 'bg-red-500'}`}
        />
        {toast.message}
      </div>
    </div>
  );
};

