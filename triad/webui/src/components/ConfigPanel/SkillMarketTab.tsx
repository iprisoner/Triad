interface CategoryTabBarProps {
  categories: string[];
  active: string;
  onSelect: (cat: string) => void;
}

const CategoryTabBar: React.FC<CategoryTabBarProps> = ({
  categories,
  active,
  onSelect,
}) => {
  const scrollRef = useRef<HTMLDivElement>(null);

  const scroll = (dir: number) => {
    if (scrollRef.current) {
      scrollRef.current.scrollBy({ left: dir * 120, behavior: 'smooth' });
    }
  };

  return (
    <div className="relative flex items-center gap-1 bg-muted/50 rounded-lg p-1.5">
      <button
        onClick={() => scroll(-1)}
        className="shrink-0 w-7 h-7 flex items-center justify-center rounded-md hover:bg-muted transition-colors text-muted-foreground"
        type="button"
        aria-label="向左滚动"
      >
        <ChevronLeft className="w-4 h-4" />
      </button>

      <div
        ref={scrollRef}
        className="flex-1 flex gap-1 overflow-x-auto scrollbar-hide"
        style={{ scrollbarWidth: 'none' }}
      >
        {categories.map((cat) => (
          <button
            key={cat}
            onClick={() => onSelect(cat)}
            className={`
              shrink-0 px-3 py-1.5 rounded-md text-sm font-medium transition-all duration-200
              ${active === cat
                ? 'bg-background text-foreground shadow-sm border border-border/50'
                : 'text-muted-foreground hover:text-foreground hover:bg-muted'
              }
            `}
            type="button"
          >
            {categoryLabels[cat] || cat}
          </button>
        ))}
      </div>

      <button
        onClick={() => scroll(1)}
        className="shrink-0 w-7 h-7 flex items-center justify-center rounded-md hover:bg-muted transition-colors text-muted-foreground"
        type="button"
        aria-label="向右滚动"
      >
        <ChevronRight className="w-4 h-4" />
      </button>
    </div>
  );
};

